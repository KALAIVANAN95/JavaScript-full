//5.Math method and Property

//i)Math.PI
console.log("It will return the PI value", Math.PI);

//ii)trunc
console.log("It will return the PI value with rounded", Math.trunc(Math.PI));

//iii)round
console.log("It will return the PI value with rounded", Math.round(Math.PI));

console.log("It will return the 4 value with rounded", Math.round(4.3));

console.log("It will return the 5 value with rounded", Math.round(4.5));

//iv)ceil
console.log("It will return ceil value", Math.ceil(4.3));

//v)floor
console.log("It will return floor value", Math.floor(4.9));

//vi)pow
console.log("This is power of value 3*3*3*3", Math.pow(3, 4));

//vii)min
console.log("It will return minimum value", Math.min(3, 4, 5, 6, 7, 2));

//viii)max
console.log("It will return maximum value", Math.max(3, 4, 5, 6, 7, 2));

//ix)random
console.log("This will return random value", Math.random());

console.log("This will return random value", Math.random() * 10);

console.log("This will return random value", Math.floor(Math.random() * 10));

console.log(
  "This will return random value",
  Math.floor(Math.random() * 10 + 1)
);
